// Variables & Data Types

// -- String --
const firstName = "Usman";
const lastName = "Khan";
console.log("Full Name:", firstName + " " + lastName);

// -- Number --
const age = 22;
const gpa = 3.3;
console.log("Age:", age);
console.log("GPA:", gpa);

// -- Boolean --
const isStudent = true;
console.log("Is a student?", isStudent);

// -- Undefined --
let futurePlan;
console.log("Future Plan:", futurePlan);

// -- Null --
const examScore = null;
console.log("Exam Score:", examScore);

// -- Object --
const person = {
    name: "Usman",
    city: "Lahore"
};
console.log("Person:", person);

// -- Array --
const favoriteColors = ["Blue", "Green", "Black"];
console.log("Favorite Colors:", favoriteColors);

// -- Function --
const greet = function() {
    return "Hello from Usman!";
};
console.log("Greeting:", greet());